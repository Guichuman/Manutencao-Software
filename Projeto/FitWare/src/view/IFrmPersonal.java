    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        personal = (ArrayList<Personal>) s.createQuery("From Personal").list();
        int conf = 0;
        for(Personal p1 : personal){
            if(p1.getCpfPersonal().equals(fTextCpf.getText())){
                JOptionPane.showMessageDialog(this, "CPF já está cadastrado!");
                conf = 1;
            }
        }
        if(conf == 0){
            if(campoVazio()){
                if(txtSenha.getText().equals(txtConfSenha.getText())){
                   a = new Acesso(fTextCpf.getText(),
                                txtSenha.getText(),
                                2);

                    p = new Personal(fTextCpf.getText(),
                                     txtNome.getText(),
                                     txtTurno.getText());
                    try{  
                        s.beginTransaction();
                        s.save(a);
                        s.save(p);
                        s.getTransaction().commit();
                        JOptionPane.showMessageDialog(this, "Cadastro realizado com sucesso!");
                        atualizarTabela(p, escolha);
                        limparCampos();
                    }catch(Exception ex){
                        JOptionPane.showMessageDialog(this, ex);
                    } 
                }
                
            }
        }
            
        
    }//GEN-LAST:event_btnCadastrarActionPerformed


