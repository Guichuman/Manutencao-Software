    public boolean campoVazio(){
        if(fTextCpf.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Preencha o campo CPF!");
            return false;
        }
        if(txtNome.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Preencha o campo nome!");
            return false;
        }
        if(txtTurno.getText().equals("")){
           JOptionPane.showMessageDialog(this, "Preencha o campo turno!");
           return false; 
        }
        if(txtSenha.getText().equals("")){
           JOptionPane.showMessageDialog(this, "Preencha o campo senha!");
           return false; 
        }
        
        return true;
    }
    
    public void limparCampos(){
        fTextCpf.setText("");
        txtNome.setText("");
        txtTurno.setText("");
        txtSenha.setText("");
        txtConfSenha.setText("");
    }
    
    private void popularTabela(){ 
        personal = (ArrayList<Personal>) s.createQuery("From Personal").list();
        dtm.setRowCount(0);        
        for(Personal pe : personal){
            dtm.addRow(pe.toRow()); 
        } 
    }
    private void atualizarTabela(Personal p, int codigo){
        if (codigo == -1){
            dtm.addRow(p.toRow());
            
        }
        else{
            dtm.removeRow(codigo); 
            dtm.insertRow(codigo, p.toRow());
        }
    }
    
    
